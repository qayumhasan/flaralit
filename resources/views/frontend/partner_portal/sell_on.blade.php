        function calculateEarnings(){
       //select elements(variables)
        var product = document.getElementById("product-type");
        var earnings = document.getElementById("earnings");
        var audience = document.getElementById("audience");
        var productValue = 1;
        var booster = 1;
    
        //get specific text values
        var productText = product.options[product.selectedIndex].text;
        var audienceText = audience.options[audience.selectedIndex].text;
    
        //check that text values are correct
        console.log(productText);
        console.log(audienceText);
    
        //assign product value
        if(productText === "Templates"){
            productValue = 972
        }else if (productText === "Online course"){
            productValue = 717
        }else if (productText === "eBook"){
            productValue = 1102
        }else if (productText === "Chrome extension"){
            productValue = 1842
        }else if (productText === "Plugin"){
            productValue = 1941
        }else if (productText === "Low-code/No-code apps"){
            productValue = 1365
        }else if (productText === "Design elements"){
            productValue = 872
        }else if (productText === "Stock photos/videos"){
            productValue = 675
        }else if (productText === "Downloadable PDF"){
            productValue = 964
        }else if (productText === "Select your product"){
            earnings.innerText = ''
        }
    
        //add boosters
        if(audienceText === "Less than 1,000"){
            booster = 1
        }else if (audienceText === "1,000-5,000"){
            booster = 1.25
        }else if (audienceText === "5,000-10,000"){
            booster = 1.5
        }else if (audienceText === "10,000-50,000"){
            booster = 1.75
        }else if (audienceText === "More than 50,000"){
            booster = 2.25
        }
    
        //calculate earnings
        var earningsNumber = booster*productValue;
        var earningsRounded = Math.round(earningsNumber);
        var earningsComma = earningsRounded.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ",");;
        earnings.innerHTML = '$' + earningsComma + '/mo';
    }
