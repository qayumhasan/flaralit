            <div class="intro-text-2">
                <h3 class="iintro-h"><strong>1. Intro</strong> (1 to 2 sentences)</h3>
                <p class="_1m-paragraph-2"><strong>Problem</strong>: Start by naming the biggest pain point your
                    users face. What annoying issue keeps coming up for them? Feel free to get descriptive, but no
                    need to get infomercial-level cheesy about it.<br>&zwj;<br><strong>Solution</strong>: Name your
                    product and introduce the way it helps solve that pain point.</p>
            </div>
            <div class="guide-right-img-2"><img
                    src="https://uploads-ssl.webflow.com/6051501504406dc96db069ac/62058802e91ea705778e0590_Intro.svg"
                    loading="lazy" alt="" class="image-170"></div>
        </div>
        <div class="intro-sec-2">
            <div class="intro-text-2">
                <h3 class="iintro-h"><strong>2. What it does </strong>(1 sentence)</h3>
                <p class="_1m-paragraph-2">Describe what the tool does, no-frills. Plainly state what the product is
                    in a nutshell:<br>&zwj;<br>This sentence usually begins with your product name, e.g.,
                    <strong>“&lt;Product&gt; is an all-in-one platform featuring project management, team chat, time
                        tracking, and reporting.”</strong>
                </p>
            </div>
            <div class="guide-right-img-2"><img
                    src="https://uploads-ssl.webflow.com/6051501504406dc96db069ac/62058802e91ea754728e0591_What%20it%20does.svg"
                    loading="lazy" alt="" class="image-170"></div>
        </div>
        <div class="intro-sec-2">
            <div class="intro-text-2">
                <h3 class="iintro-h"><strong>3. Features </strong> (3-5 sentences)</h3>
                <p class="_1m-paragraph-2">Tell us about your product’s features in order of
                    importance.<br><br>Describe each feature in 1 to 2 sentences. Each feature should have room to
                    breathe with a paragraph space. That way, this section isn’t just a block of text.</p>
            </div>
            <div class="guide-right-img-2"><img
                    src="https://uploads-ssl.webflow.com/6051501504406dc96db069ac/62058802e91ea7f3248e0592_Features.svg"
                    loading="lazy" alt="" class="image-170"></div>
        </div>
        <div class="intro-sec-2">
            <div class="intro-text-2 writing-abt">
                <h3 class="iintro-h"><strong class="bold-text-31">Writing about product features</strong></h3>
                <p class="_1m-paragraph-2">Hit the highlights. Don’t worry about mentioning every single spec—that’s
                    what the product terms and key features are for.<br><br>Avoid stats that cannot be proven. It’s
                    tempting, but don’t use words like “guarantee” or “best.” Stay away from claims like “This will
                    10x your productivity,” “increase X by 85%,” or “this is THE best deal ever.<br><br>”Also, don’t
                    mention direct competitors. You can namedrop integrations, but avoid claims about similar
                    products in your industry, such as “the better alternative to PayPal” or “10x more effective
                    than Mailchimp.” Show how you’re better—don’t tell it.<br><br>Write everything out in complete
                    sentences. No bullet points.<br><br>Our cardinal rule: Make jokes, but never make fun of the
                    audience.<br><br>Software creators: Don’t forget to mention important underlying technology,
                    features, or integrations that customers might enjoy. Sumo-lings especially love
                    A.I./automation, GPT-3, white labeling, and the ability to use CNAME records. Their priorities
                    are usually to be more productive (save time), offer their clients more value, and be able to
                    integrate tools into their existing workflows.</p>
            </div>
        </div>
        <div class="intro-sec-2">
            <div class="intro-text-2">
                <h3 class="iintro-h"><strong>4. Conclusion</strong> &nbsp;(3-5 sentences)</h3>
                <p class="_1m-paragraph-2">Circle back to your intro, adding a call-to-action:<br>&zwj;<br>Restate
                    how your product solves the pain point clearly and succinctly.<br>&zwj;<br>End with a strong
                    call-to-action that will have readers itching to purchase your product, e.g., “Accelerate your
                    project management today!”</p>
            </div>
            <div class="guide-right-img-2"><img
                    src="https://uploads-ssl.webflow.com/6051501504406dc96db069ac/62058802e91ea73efe8e0593_Conclusion.svg"
                    loading="lazy" alt="" class="image-170"></div>
        </div>
        <div class="intro-sec-2">
            <div class="intro-text-2">
                <h3 class="iintro-h"><strong>5. Edit &nbsp;</strong>(2 editing sweeps)</h3>
                <p class="_1m-paragraph-2">Once it’s written, review your copy.<br>&zwj;<br><strong>Edit for
                        clarity</strong>: Can you simplify anything? Can you turn long sentences into two short
                    sentences? Did you run your copy through a spelling/grammar check?<br>&zwj;<br><strong>Edit for
                        specificity</strong>: Can you get more specific? What makes your product different from
                    anything else on the market? What are the stakes (the real pain point) if someone doesn’t buy
                    your product?</p>
            </div>
